# This is an easy way to do mathmatical operations in ruby
puts 3.0 + 2.0 + 1.0 - 5.0 + 4.0 % 2.0 -1.0 / 4.0 + 6.0

# This prints a string:
puts "Is it true that 3 + 2 < 5 - 7?"

# This carries out the operation seen below.
puts 3.0 + 2.0 < 5.0 - 7.0

# This puts a string with a mathmatical operation inside.
# The operation is carried out and printed inside the string.
puts "What is 3 + 2? #{3.0 + 2.0}"

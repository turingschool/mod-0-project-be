# This #{} inserts computational code into a string.
# This +/- adds and subtracts. This / divides. This * multiplies.
# This is the modulo operator.
# It returns the **remainder** of the numerator divided by the denomenator.
puts "Hens #{25.0 + 30.0 / 6}"
puts "Roosters #{100.0 - 25.0 * 3 % 4}"

# This puts a string.
